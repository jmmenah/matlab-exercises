%Programa que calcula la raiz cuadrada, el logaritmo y la tangente de un
%numero
%Datos
Numero= input('Introduzca un numero: ');
%Calculos
Raiz= sqrt(Numero);
Logaritmo= log(Numero);
Tangente= tan(Numero);
%Resultados
disp(['Su raiz es ',num2str(Raiz),' su logaritmo natural es ',num2str(Logaritmo),' y su tangente ',num2str(Tangente)])