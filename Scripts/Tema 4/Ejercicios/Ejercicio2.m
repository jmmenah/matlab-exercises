%Este programa calcula la suma, resta, multiplicacion y división de dos
%numeros
%Datos
N1= input('Introduzca el primer numero: ');
N2= input('Intorduzca el segundo numero: ');
%Calculos
Suma=N1+N2;
Resta=N1-N2;
Producto=N1*N2;
Division=N1/N2;
%Resultados
disp(['La suma de los numeros es ',num2str(Suma),' la resta ',num2str(Resta),' el producto ',num2str(Producto),' y la division ',num2str(Division)])